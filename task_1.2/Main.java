/*
Закомментируй ненужные строки кода, чтобы на экран вывелась надпись:
2 плюс 3 равно 5
Примечание: комментировать строки с объявлением переменных нельзя.
*/
// Пример комментария

public class Main {
    public static void main(String[] args) {
        int a = 3;
        int b = 2;

        System.out.print("два"); // Ненужная строка
        System.out.print(b);
        System.out.print(" плюс ");
        System.out.print(" минус "); // Ненужная строка
        System.out.print(a);
        System.out.print("три"); // Ненужная строка
        System.out.print(" равно ");
        System.out.print(" будет "); // Ненужная строка
        System.out.print("пять"); // Ненужная строка
        System.out.print(a + b);
    }
}
